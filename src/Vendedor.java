
public class Vendedor {
	private String nomeVend;
	private String cpfVend;

	
	
	//GETTERS AND SETTERS
	public void setNome(String nome) {
		this.nomeVend = nome;
	}
		
	public void setCpf(String cpf) {
		this.cpfVend = cpf;
	}
		
	public String getNome() {
		return this.nomeVend;
	}
		
	public String getCpf() {
		return this.cpfVend;
	}
}



